#!/bin/bash
# Certian Node module files are needed for production

# find out if dist/js directory exists, if not create it
if [ -f dist/js/ ]
then
  echo "The dist/js directory already exists"
else
  mkdir dist/js
fi

# Copy certain node modules scripts to dist folder
# Add additional js files to the scripts array
scripts=(
  "hoverintent/dist/hoverintent.min.js"
)

for i in "${scripts[@]}"
do
  cp node_modules/$i dist/js/.
done
